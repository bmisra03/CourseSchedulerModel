    private void dropCourseButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dropCourseButtonActionPerformed
        // TODO add your handling code here:
        CourseEntry course = (CourseEntry)selectCourseComboBox2.getSelectedItem();
        ArrayList<ScheduleEntry> scheduled = ScheduleQueries.getScheduledStudentsByCourse(
                currentSemester, course.getCourseCode());
        ArrayList<ScheduleEntry> waitlisted = ScheduleQueries.getWaitlistedStudentsByCourse(
                currentSemester, course.getCourseCode());
        droppedCourseTextArea.append("Scheduled students dropped from the course: \n");
        for (ScheduleEntry entry: scheduled){
            droppedCourseTextArea.append(StudentQueries.getStudent(
                    entry.getStudentID()).toString()+"\n");
        }
        droppedCourseTextArea.append("\nWaitlisted students dropped from the course: \n");
        for (ScheduleEntry entry: waitlisted){
            droppedCourseTextArea.append(StudentQueries.getStudent(
                    entry.getStudentID()).toString()+"\n");
        }
        ScheduleQueries.dropScheduleByCourse(currentSemester, course.getCourseCode());
        CourseQueries.dropCourse(currentSemester, course.getCourseCode());
        rebuildCourseComboBoxes();
    }//GEN-LAST:event_dropCourseButtonActionPerformed

