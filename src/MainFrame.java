    private void displayStudentCoursesButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_displayStudentCoursesButtonActionPerformed
        // TODO add your handling code here:
        if(currentStudent==null){
            JOptionPane.showMessageDialog(null, 
                    "Error: add a student before displaying schedule", "Error", JOptionPane.ERROR_MESSAGE);
        }
        else{
            ArrayList<ScheduleEntry> schedule = ScheduleQueries.getScheduleByStudent(currentSemester,
                currentStudent.getStudentID());
            DefaultTableModel displayScheduleTableModel = (DefaultTableModel)displayScheduleTable.getModel();
            displayScheduleTableModel.setNumRows(0);
            Object[] rowData = new Object[2];
            for(ScheduleEntry entry: schedule){
                rowData[0]=entry.getCourseCode();
                if("S".equals(entry.getStatus())){
                    rowData[1]="Scheduled";
                }
                else{
                    rowData[1]="Waitlisted";
                }
                displayScheduleTableModel.insertRow(0,rowData);
            }
        }
    }//GEN-LAST:event_displayStudentCoursesButtonActionPerformed

    private void scheduleCourseButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_scheduleCourseButtonActionPerformed
        // TODO add your handling code here:
        CourseEntry course = (CourseEntry) selectCourseComboBox.getSelectedItem();
        String status;
        if (ScheduleQueries.getScheduledStudentCount(currentSemester, course.getCourseCode())<course.getSeats()){
            status = "S";
            scheduleCourseStatusLabel.setText(currentStudent+" has been scheduled for "+course.getCourseCode());
        } else{
            status = "W";
            scheduleCourseStatusLabel.setText(currentStudent+" has been waitlisted for "+course.getCourseCode());
        }
        Date date = new Date();
        Timestamp ts=new Timestamp(date.getTime());
        ScheduleEntry entry = new ScheduleEntry(currentSemester, course.getCourseCode(),
            currentStudent.getStudentID(),status,ts);
        ScheduleQueries.addScheduleEntry(entry);
    }//GEN-LAST:event_scheduleCourseButtonActionPerformed

